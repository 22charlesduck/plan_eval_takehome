#!/usr/bin/env python3
"""
LLM-as-Judge Evaluation of Plan Quality

Calls Claude API to score each plan on 5 quality dimensions (1-5 scale):
  1. Specificity     - Does the plan name specific file paths?
  2. Reasoning depth - Does the plan explain WHY each file is relevant?
  3. Completeness    - Does the plan consider edge cases, tests, dependencies?
  4. Actionability   - Could a developer follow these steps?
  5. Risk awareness  - Does the plan identify potential problems?

Saves scores to data/judge_scores.json (or data/judge_scores_extended.json).
Computes correlation between judge scores and actual file recall.

Usage:
  python scripts/llm_judge.py                              # pilot data
  python scripts/llm_judge.py --plans data/plans_extended.json --metrics data/metrics_extended.json --output data/judge_scores_extended.json
"""

import os
os.chdir("/scr/clding/plan_eval")

import json
import re
import time
import argparse
import traceback

from dotenv import load_dotenv
load_dotenv()

import anthropic
import numpy as np
from scipy import stats

client = anthropic.Anthropic()

MODEL = "claude-sonnet-4-20250514"
MAX_TOKENS = 1024
RATE_LIMIT_DELAY = 0.5

JUDGE_PROMPT = """\
You are evaluating the quality of a coding plan generated by an AI agent. The plan was created to address a GitHub issue, using only the issue description and the repository file tree (no code access).

Score each dimension on a 1-5 scale:
- 1 = Very poor
- 2 = Below average
- 3 = Average
- 4 = Good
- 5 = Excellent

## The Task (Issue Description)
{problem_statement}

## The Plan
{plan_json}

## Scoring Dimensions

1. **Specificity** (1-5): Does the plan name specific file paths from the repository, or does it use vague module/component references? Higher = more specific paths named.

2. **Reasoning depth** (1-5): Does the plan explain WHY each file is relevant to the fix, or does it just list files? Higher = clearer reasoning about each file's role.

3. **Completeness** (1-5): Does the plan consider edge cases, test files, downstream dependencies, and potential side effects? Higher = more thorough coverage.

4. **Actionability** (1-5): Could a developer follow these implementation steps without needing to do significant additional research? Higher = more actionable steps.

5. **Risk awareness** (1-5): Does the plan identify potential problems, wrong turns, or things that could go wrong? Higher = better risk identification.

Respond in EXACTLY this JSON format (no other text):
{{"specificity": N, "reasoning_depth": N, "completeness": N, "actionability": N, "risk_awareness": N, "overall": N, "brief_rationale": "one sentence summary of plan quality"}}

where N is an integer 1-5 and "overall" is your overall quality score (1-5), which may differ from the average of the dimensions.
"""


def parse_json_response(text: str) -> dict:
    """Parse JSON from model response, stripping markdown code fences if present."""
    md_match = re.search(r"```(?:json)?\s*\n?(.*?)\n?\s*```", text, re.DOTALL)
    if md_match:
        json_str = md_match.group(1).strip()
    else:
        json_str = text.strip()
    return json.loads(json_str)


def judge_plan(task_id: str, problem_statement: str, plan: dict,
               index: int, total: int, max_retries: int = 2) -> dict:
    """Call Claude API to judge a single plan."""
    print(f"  [{index}/{total}] Judging plan for {task_id}...")

    # Format plan as readable JSON (trim raw_response if present)
    plan_display = {k: v for k, v in plan.items() if k != "raw_response"}
    plan_json_str = json.dumps(plan_display, indent=2)

    # Truncate problem statement if extremely long (keep first 3000 chars)
    if len(problem_statement) > 3000:
        problem_statement = problem_statement[:3000] + "\n\n... [truncated for length] ..."

    prompt = JUDGE_PROMPT.format(
        problem_statement=problem_statement,
        plan_json=plan_json_str,
    )

    last_error = None
    raw_text = None

    for attempt in range(max_retries + 1):
        try:
            if attempt > 0:
                print(f"           -> Retry {attempt}/{max_retries}...")
                time.sleep(2 ** attempt)

            response = client.messages.create(
                model=MODEL,
                max_tokens=MAX_TOKENS,
                messages=[{"role": "user", "content": prompt}],
            )

            raw_text = response.content[0].text
            scores = parse_json_response(raw_text)

            # Validate scores
            required_dims = ["specificity", "reasoning_depth", "completeness",
                             "actionability", "risk_awareness", "overall"]
            for dim in required_dims:
                if dim not in scores:
                    raise ValueError(f"Missing dimension: {dim}")
                if dim != "brief_rationale":
                    val = scores[dim]
                    if not isinstance(val, (int, float)) or val < 1 or val > 5:
                        raise ValueError(f"Invalid score for {dim}: {val} (must be 1-5)")

            print(f"           -> scores: spec={scores['specificity']} "
                  f"depth={scores['reasoning_depth']} comp={scores['completeness']} "
                  f"action={scores['actionability']} risk={scores['risk_awareness']} "
                  f"overall={scores['overall']}")

            return {
                "task_id": task_id,
                "scores": scores,
                "raw_response": raw_text,
            }

        except json.JSONDecodeError as e:
            last_error = e
            print(f"           -> JSON parse error on attempt {attempt + 1}: {e}")
            if raw_text:
                print(f"           -> Raw: {raw_text[:200]}...")
        except anthropic.APIError as e:
            last_error = e
            print(f"           -> API error on attempt {attempt + 1}: {e}")
        except Exception as e:
            last_error = e
            print(f"           -> Error on attempt {attempt + 1}: {e}")

    # All retries exhausted
    return {
        "task_id": task_id,
        "scores": None,
        "raw_response": raw_text,
        "error": str(last_error),
    }


def analyze_judge_recall_correlation(judge_results: list, metrics: list):
    """Compute correlation between judge scores and actual file recall."""
    # Index metrics by task_id
    recall_by_task = {m["task_id"]: m["plan_file_recall"] for m in metrics}

    # Collect paired data
    dimensions = ["specificity", "reasoning_depth", "completeness",
                  "actionability", "risk_awareness", "overall"]

    dim_data = {dim: {"scores": [], "recalls": []} for dim in dimensions}

    for result in judge_results:
        task_id = result["task_id"]
        scores = result.get("scores")
        if scores is None:
            continue
        if task_id not in recall_by_task:
            continue

        recall = recall_by_task[task_id]
        for dim in dimensions:
            if dim in scores and isinstance(scores[dim], (int, float)):
                dim_data[dim]["scores"].append(scores[dim])
                dim_data[dim]["recalls"].append(recall)

    print("\n" + "=" * 70)
    print("JUDGE SCORE vs ACTUAL RECALL CORRELATION ANALYSIS")
    print("=" * 70)
    print(f"  {'Dimension':<20} {'Pearson r':>10} {'p-value':>10} {'Spearman rho':>13} {'p-value':>10}")
    print("  " + "-" * 65)

    best_dim = None
    best_corr = -999

    for dim in dimensions:
        scores_arr = np.array(dim_data[dim]["scores"])
        recalls_arr = np.array(dim_data[dim]["recalls"])

        if len(scores_arr) < 3:
            print(f"  {dim:<20} insufficient data (n={len(scores_arr)})")
            continue

        pearson_r, pearson_p = stats.pearsonr(scores_arr, recalls_arr)
        spearman_r, spearman_p = stats.spearmanr(scores_arr, recalls_arr)

        print(f"  {dim:<20} {pearson_r:>+10.3f} {pearson_p:>10.4f} {spearman_r:>+13.3f} {spearman_p:>10.4f}")

        if abs(pearson_r) > best_corr:
            best_corr = abs(pearson_r)
            best_dim = dim

    print("  " + "-" * 65)
    if best_dim:
        print(f"  Best predictor of recall: {best_dim} (|Pearson r| = {best_corr:.3f})")
    print("=" * 70)

    return best_dim, best_corr


def main():
    parser = argparse.ArgumentParser(description="LLM-as-judge plan quality evaluation")
    parser.add_argument("--plans", default="data/plans.json",
                        help="Path to plans JSON file")
    parser.add_argument("--tasks", default="data/tasks.json",
                        help="Path to tasks JSON file (for problem statements)")
    parser.add_argument("--metrics", default="data/metrics.json",
                        help="Path to metrics JSON file (for recall correlation)")
    parser.add_argument("--output", default="data/judge_scores.json",
                        help="Path to output judge scores JSON file")
    args = parser.parse_args()

    # Load plans
    if not os.path.exists(args.plans):
        print(f"ERROR: {args.plans} not found.")
        return
    with open(args.plans) as f:
        plans = json.load(f)

    # Load tasks for problem statements
    if not os.path.exists(args.tasks):
        print(f"ERROR: {args.tasks} not found.")
        return
    with open(args.tasks) as f:
        tasks = json.load(f)

    task_problem = {t["task_id"]: t["problem_statement"] for t in tasks}

    # Filter to plans that have valid plan data
    valid_plans = [p for p in plans if p.get("plan") is not None]
    total = len(valid_plans)
    print(f"Loaded {len(plans)} plans, {total} have valid plan data")

    # Load existing judge scores for resume support
    existing = {}
    if os.path.exists(args.output):
        with open(args.output) as f:
            existing_list = json.load(f)
        for entry in existing_list:
            if entry.get("scores") is not None:
                existing[entry["task_id"]] = entry
        print(f"Found {len(existing)} existing judge scores -- will resume\n")
    else:
        print("No existing judge scores found -- judging all plans\n")

    results = list(existing.values())
    existing_ids = set(existing.keys())
    successes = len(existing)
    failures = 0

    for i, plan_entry in enumerate(valid_plans, 1):
        task_id = plan_entry["task_id"]

        if task_id in existing_ids:
            continue

        problem = task_problem.get(task_id, "")
        if not problem:
            print(f"  [{i}/{total}] WARNING: No problem statement for {task_id}, skipping")
            continue

        result = judge_plan(task_id, problem, plan_entry["plan"], i, total)
        results.append(result)

        if result.get("scores") is not None:
            successes += 1
        else:
            failures += 1

        # Save progress every 5 tasks
        if (i - len(existing)) % 5 == 0:
            with open(args.output, "w") as f:
                json.dump(results, f, indent=2)

        # Rate limiting
        if i < total:
            time.sleep(RATE_LIMIT_DELAY)

    # Final save
    with open(args.output, "w") as f:
        json.dump(results, f, indent=2)

    print(f"\nDone. {successes} judged, {failures} failed.")
    print(f"Saved to {args.output}")

    # Print score summary
    scored = [r for r in results if r.get("scores") is not None]
    if scored:
        dims = ["specificity", "reasoning_depth", "completeness",
                "actionability", "risk_awareness", "overall"]

        print(f"\n{'='*60}")
        print("JUDGE SCORE SUMMARY")
        print(f"{'='*60}")
        print(f"  {'Dimension':<20} {'Mean':>7}  {'Median':>7}  {'Std':>7}")
        print("  " + "-" * 45)
        for dim in dims:
            vals = [r["scores"][dim] for r in scored if dim in r["scores"]]
            if vals:
                arr = np.array(vals)
                print(f"  {dim:<20} {arr.mean():>7.2f}  {np.median(arr):>7.1f}  {arr.std():>7.2f}")
        print(f"{'='*60}")

    # Correlation analysis
    if os.path.exists(args.metrics):
        with open(args.metrics) as f:
            metrics = json.load(f)
        analyze_judge_recall_correlation(results, metrics)
    else:
        print(f"\n  Skipping correlation analysis: {args.metrics} not found.")


if __name__ == "__main__":
    main()
